s = list(input())
left = 0
right = len(s) - 1
while left < right:
    s[left], s[right] = s[right], s[left]
    left += 1
    right -= 1
print(s)

#为了反转字符串，我们可以使用双指针的方法。我们设置两个指针，一个指向字符串的开头，另一个指向字符串的末尾。然后我们交换这两个指针所指向的字符，并将左指针向右移动，右指针向左移动。重复这个过程，直到两个指针相遇或交错。时间复杂度是O(n)，其中n是字符串的长度。空间复杂度是O(1)，因为我们只使用了常数级别的额外空间来存储指针和临时变量。
#文字列を反転するために、双方向のポインタを使用する方法があります。最初のポインタは文字列の先頭を指し、もう一方のポインタは文字列の末尾を指します。これらのポインタが指す文字を交換し、左のポインタを右に移動し、右のポインタを左に移動します。このプロセスを繰り返し、両方のポインタが出会うか交差するまで続けます。時間複雑度はO(n)で、nは文字列の長さです。空間複雑度はO(1)で、定数の追加スペースのみを使用します。