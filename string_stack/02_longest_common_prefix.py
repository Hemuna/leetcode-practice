n = int(input())
strs = input().split()
if not strs:
    print("")
else:
    prefix = strs[0]
    for s in strs[1:]:
        while  s[:len(prefix)] != prefix:
            prefix = prefix[:-1]
            if prefix == "":
                break
    print(prefix)

#为了找到字符串数组中的最长公共前缀，我们可以首先将第一个字符串作为初始的前缀，然后依次与后续的字符串进行比较。如果当前前缀不是下一个字符串的前缀，我们就逐渐缩短前缀，直到找到一个匹配或者前缀变为空。最终，剩下的前缀就是最长公共前缀。时间复杂度是O(n*m)，其中n是字符串的数量，m是字符串的平均长度。空间复杂度是O(1)，因为我们只使用了常数级别的额外空间来存储前缀。
#文字列配列の中で最も長い共通の接頭辞を見つけるために、最初の文字列を初期の接頭辞として使用し、次の文字列と比較します。もし現在の接頭辞が次の文字列の接頭辞でない場合は、接頭辞を徐々に短くしていきます。最終的に残った接頭辞が最も長い共通の接頭辞になります。時間複雑度はO(n*m)で、nは文字列の数、mは文字列の平均長さです。空間複雑度はO(1)で、定数の追加スペースのみを使用します。

