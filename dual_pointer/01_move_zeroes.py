nums = list(map(int, input().split()))
left = 0
for right in range(len(nums)):
    if nums[right] != 0:
        nums[left], nums[right] = nums[right], nums[left]
        left += 1
print(nums)

#为了将数组中的零移动到末尾，我们可以使用双指针的方法。我们设置一个指针left来跟踪非零元素的位置，另一个指针right来遍历数组。当right指向的元素不为零时，我们将其与left指向的元素交换，并将left向右移动。这样，所有非零元素都会被移动到数组的前面，而零会被移动到后面。时间复杂度是O(n)，其中n是数组的长度。空间复杂度是O(1)，因为我们只使用了常数级别的额外空间来存储指针和临时变量。
#配列内のゼロを末尾に移動するために、双方向のポインタを使用する方法があります。leftポインタは非ゼロ要素の位置を追跡し、rightポインタは配列を走査します。rightが指す要素がゼロでない場合、その要素をleftが指す要素と交換し、leftを右に移動します。これにより、すべての非ゼロ要素が配列の前に移動し、ゼロは後ろに移動します。時間複雑度はO(n)で、nは配列の長さです。空間複雑度はO(1)で、定数の追加スペースのみを使用します。
